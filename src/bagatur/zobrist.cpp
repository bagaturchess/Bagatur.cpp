void init() {
    static bool done = false;
    if (done) return;
    done = true;

    std::size_t counter = 0;
    for (int sq = 0; sq < 64; ++sq) {
        for (int color = 0; color < 2; ++color) {
            for (int p = 0; p < 7; ++p) {
                piece[sq][color][p] = static_cast<BB>(NUMBERS[counter++]);
            }
        }
    }
    for (std::size_t i = 0; i < castling.size(); ++i)
        castling[i] = static_cast<BB>(NUMBERS[counter++]);

    // Skip epIndex[0]: Java keeps it 0 to make the XOR a no-op.
    for (std::size_t i = 1; i < epIndex.size(); ++i)
        epIndex[i] = static_cast<BB>(NUMBERS[counter++]);

    sideToMove = static_cast<BB>(NUMBERS[counter++]);
}

